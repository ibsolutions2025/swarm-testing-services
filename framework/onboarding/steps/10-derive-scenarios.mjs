/**
 * Step 10 — derive-scenarios.
 *
 * Sonnet enumerates the lifecycle scenarios the protocol can produce.
 * Inputs: contract source for primary contract, event names from step 08,
 * state-changing function names from primary ABI.
 *
 * Output: lib/<slug>/scenarios.ts.
 */

import { callSonnetJson } from "../lib/llm.mjs";
import { fetchContractSource } from "../lib/source-fetcher.mjs";
import { writeFile, mkdir, readFile } from "node:fs/promises";
import { resolve, dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = dirname(fileURLToPath(import.meta.url));

function renderScenariosTs({ slug, manifestUrl, scenarios }) {
  const header = [
    `// AUTO-GENERATED by framework/onboarding/engine.mjs step 10-derive-scenarios`,
    `// Source: ${manifestUrl}`,
    `// Generated: ${new Date().toISOString()}`,
    `// Slug: ${slug}`,
    `// Coverage: ${scenarios.length} scenarios derived by LLM from contract source`,
    ``,
  ].join("\n");

  return header + [
    `export type ScenarioStatus = "classifiable" | "aspirational" | "deferred" | "in-flight";`,
    ``,
    `export interface Scenario {`,
    `  id: string;`,
    `  label: string;`,
    `  description: string;`,
    `  status: ScenarioStatus;`,
    `  applicability: string;`,
    `  requiredEvents?: string[];`,
    `  negativeEvents?: string[];`,
    `  terminalState?: Record<string, unknown>;`,
    `  notes?: string;`,
    `}`,
    ``,
    `export const ALL_SCENARIOS: Scenario[] = ${JSON.stringify(scenarios, null, 2)};`,
    ``,
    `const BY_ID: Record<string, Scenario> = (() => {`,
    `  const map: Record<string, Scenario> = {};`,
    `  for (const s of ALL_SCENARIOS) map[s.id] = s;`,
    `  return map;`,
    `})();`,
    ``,
    `export function getScenarioById(id: string): Scenario | undefined { return BY_ID[id]; }`,
    `export function isScenarioClassifiable(id: string): boolean { return BY_ID[id]?.status === "classifiable"; }`,
    ``,
    `export const CLASSIFIABLE_SCENARIO_IDS = ALL_SCENARIOS.filter((s) => s.status === "classifiable" && s.id !== "s00-in-flight").map((s) => s.id);`,
    `export const ASPIRATIONAL_SCENARIO_IDS = ALL_SCENARIOS.filter((s) => s.status === "aspirational").map((s) => s.id);`,
    `export const SCENARIO_COUNT = ALL_SCENARIOS.length;`,
    ``,
  ].join("\n");
}

export async function run(ctx) {
  const slug = ctx.steps?.["02-discover-manifest"]?.output?.slug;
  const manifestUrl = ctx.steps?.["02-discover-manifest"]?.output?.manifestUrl;
  const inv = ctx.steps?.["03-inventory-contracts"]?.output;
  const eventNames = ctx.steps?.["08-generate-events"]?.output?.eventNames || [];
  if (!slug || !inv) return { ok: false, error: "step 10 needs slug + inventory" };

  // Pick primary contract — same heuristic as step 09 (read from ctx.outDir)
  const baseDir = ctx.outDir || ctx.repoRoot;
  const contractsTsPath = resolve(baseDir, "lib", slug, "contracts.ts");
  const src = await readFile(contractsTsPath, "utf8");
  const re = /export const (\w+_ABI)\s*=\s*\[([\s\S]*?)\] as const;/g;
  const abiBundle = {};
  let m;
  while ((m = re.exec(src)) !== null) {
    try { abiBundle[m[1]] = JSON.parse(`[${m[2]}]`); } catch {}
  }
  let best = null, bestScore = -1;
  for (const c of inv.contracts) {
    const an = c.name.replace(/([a-z0-9])([A-Z])/g, "$1_$2").toUpperCase() + "_ABI";
    const abi = abiBundle[an];
    if (!Array.isArray(abi)) continue;
    const score = abi.filter((e) => e.type === "function").length;
    if (score > bestScore) { bestScore = score; best = { c, abi }; }
  }
  if (!best) return { ok: false, error: "no primary contract abi" };

  const fnNames = best.abi
    .filter((e) => e.type === "function" && e.stateMutability !== "view" && e.stateMutability !== "pure")
    .map((e) => e.name);

  const manifest = ctx.steps?.["02-discover-manifest"]?.output?.manifest || {};
  const sourceRes = await fetchContractSource(best.c.chainId, best.c.address, {
    contractName: best.c.name,
    manifestRepository: manifest.repository || manifest.repo || null,
    mcpServerRepo: manifest.integration?.mcp_server?.repo || null,
  });
  const srcExcerpt = sourceRes.ok
    ? Object.entries(sourceRes.files).map(([f, b]) => `// ===== ${f} =====\n${b}`).join("\n\n").slice(0, 70000)
    : "(source unavailable)";

  const promptPath = resolve(__dirname, "..", "prompts", "derive-scenarios.md");
  const promptTemplate = await readFile(promptPath, "utf8");
  const userPrompt = `${promptTemplate}\n\nPrimary contract: ${best.c.name}\n` +
    `State-changing functions: ${fnNames.join(", ")}\n\n` +
    `Event names from full inventory: ${eventNames.join(", ")}\n\n` +
    `Contract source (excerpt):\n${srcExcerpt}`;

  const llm = await callSonnetJson({
    system: "You are a precise protocol-lifecycle analyst. Output only valid JSON of the requested shape.",
    user: userPrompt,
    maxTokens: 16000,
    label: "derive-scenarios",
  });
  if (!llm.ok) return { ok: false, error: `LLM failed: ${llm.error?.slice(0, 300)}` };

  let scenarios = Array.isArray(llm.value?.scenarios) ? llm.value.scenarios : [];
  // Ensure s00-in-flight is present
  if (!scenarios.find((s) => s.id === "s00-in-flight")) {
    scenarios = [
      { id: "s00-in-flight", label: "In-flight", description: "Job exists but has not reached a terminal state yet — catch-all bucket for active jobs.", status: "in-flight", applicability: "any", notes: "Not a terminal classification." },
      ...scenarios,
    ];
  }

  const tsSource = renderScenariosTs({ slug, manifestUrl, scenarios });
  const outPath = resolve(baseDir, "lib", slug, "scenarios.ts");
  await writeFile(outPath, tsSource, "utf8");

  return {
    ok: true,
    output: {
      scenariosTsPath: outPath,
      scenarioCount: scenarios.length,
      classifiableCount: scenarios.filter((s) => s.status === "classifiable").length,
      aspirationalCount: scenarios.filter((s) => s.status === "aspirational").length,
      deferredCount: scenarios.filter((s) => s.status === "deferred").length,
      ids: scenarios.map((s) => s.id),
      usage: llm.usage,
    },
  };
}
