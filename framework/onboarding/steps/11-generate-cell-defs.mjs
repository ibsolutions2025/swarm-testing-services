/**
 * Step 11 — generate-cell-defs.
 *
 * For each classifiable scenario from step 10, ask the LLM to write a
 * predicate function over the scanner's classification context. Emit
 * lib/<slug>/cell-defs.ts.
 *
 * The LLM returns predicate BODIES (one per scenario) + a priority order.
 * The engine wraps them into a TypeScript module that mirrors lib/awp/cell-defs.ts:
 *   PREDICATES: Record<scenarioId, (ctx) => boolean>
 *   PRIORITY:   string[]
 *   classify(ctx): string
 */

import { callSonnetJson } from "../lib/llm.mjs";
import { writeFile, readFile } from "node:fs/promises";
import { resolve, dirname } from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = dirname(fileURLToPath(import.meta.url));

function renderCellDefsTs({ slug, manifestUrl, predicates, priority, classifiableIds, unclassifiableIds }) {
  const header = [
    `// AUTO-GENERATED by framework/onboarding/engine.mjs step 11-generate-cell-defs`,
    `// Source: ${manifestUrl}`,
    `// Generated: ${new Date().toISOString()}`,
    `// Slug: ${slug}`,
    `// Predicates: ${Object.keys(predicates).length} total — ${classifiableIds.length} classifiable, ${unclassifiableIds.length} catalog-only stubs`,
    `//`,
    `// classifiable=true predicates are walked by classify() at scan time.`,
    `// classifiable=false predicates are stubs (return false) for scenarios the`,
    `// scanner cannot detect from event logs alone (debug_traceTransaction needed,`,
    `// internal state reads required, etc.). They exist for catalog completeness.`,
    ``,
    `export const ZERO_ADDRESS = "0x0000000000000000000000000000000000000000" as const;`,
    ``,
    `export interface ClassificationContext {`,
    `  job: { status: number; activeValidator: string; [k: string]: unknown };`,
    `  submissions: Array<{ worker: string; status: number; [k: string]: unknown }>;`,
    `  events: Record<string, Array<{ blockNumber: number; logIndex?: number; txHash: string }>>;`,
    `  counts: { approved: number; rejected: number; pending: number; notSel: number; distinctWorkers: number; all_rejected: boolean };`,
    `  configParams: Record<string, unknown>;`,
    `}`,
    ``,
    `export type ScenarioPredicate = (ctx: ClassificationContext) => boolean;`,
    ``,
  ].join("\n");

  const predicateLines = Object.entries(predicates).map(([id, body]) =>
    `  ${JSON.stringify(id)}: ${body}`
  ).join(",\n");

  return header + [
    `export const PREDICATES: Record<string, ScenarioPredicate> = {`,
    predicateLines,
    `};`,
    ``,
    `export const PRIORITY: string[] = ${JSON.stringify(priority, null, 2)};`,
    ``,
    `// Predicates listed here are stubs — scanner does NOT walk them. Kept for catalog parity.`,
    `export const UNCLASSIFIABLE_SCENARIO_IDS: string[] = ${JSON.stringify(unclassifiableIds, null, 2)};`,
    ``,
    `export function classify(ctx: ClassificationContext): string {`,
    `  for (const key of PRIORITY) {`,
    `    const pred = PREDICATES[key];`,
    `    try {`,
    `      if (pred && pred(ctx)) return key;`,
    `    } catch { /* missing field — skip */ }`,
    `  }`,
    `  if (ctx.job.status === 2 || ctx.job.status === 3) return "unclassified";`,
    `  return "s00-in-flight";`,
    `}`,
    ``,
    `export function computeCounts(submissions: Array<{ worker: string; status: number }>) {`,
    `  return {`,
    `    approved: submissions.filter((s) => s.status === 1).length,`,
    `    rejected: submissions.filter((s) => s.status === 2).length,`,
    `    pending: submissions.filter((s) => s.status === 0).length,`,
    `    notSel: submissions.filter((s) => s.status === 3).length,`,
    `    distinctWorkers: new Set(submissions.map((s) => s.worker.toLowerCase())).size,`,
    `    all_rejected: submissions.length > 0 && submissions.every((s) => s.status === 2),`,
    `  };`,
    `}`,
    ``,
    `export const CLASSIFIABLE_SCENARIO_IDS = ${JSON.stringify(classifiableIds, null, 2)};`,
    ``,
  ].join("\n");
}

export async function run(ctx) {
  const slug = ctx.steps?.["02-discover-manifest"]?.output?.slug;
  const manifestUrl = ctx.steps?.["02-discover-manifest"]?.output?.manifestUrl;
  const eventNames = ctx.steps?.["08-generate-events"]?.output?.eventNames || [];
  const scenariosTsPath = ctx.steps?.["10-derive-scenarios"]?.output?.scenariosTsPath;
  const axisNames = ctx.steps?.["09-derive-matrix"]?.output?.axisNames || [];
  if (!slug || !scenariosTsPath) return { ok: false, error: "step 11 needs slug + scenarios" };

  // Read scenarios from emitted file
  const scenariosSrc = await readFile(scenariosTsPath, "utf8");
  const m = scenariosSrc.match(/export const ALL_SCENARIOS:[^=]*=\s*(\[[\s\S]*?\]);/);
  if (!m) return { ok: false, error: "could not parse scenarios.ts" };
  const scenarios = JSON.parse(m[1]);
  const classifiable = scenarios.filter((s) => s.status === "classifiable" && s.id !== "s00-in-flight");

  const promptPath = resolve(__dirname, "..", "prompts", "generate-cell-defs.md");
  const promptTemplate = await readFile(promptPath, "utf8");
  const userPrompt =
    `${promptTemplate}\n\n` +
    `Event names available: ${eventNames.join(", ")}\n` +
    `Config-axis names: ${axisNames.join(", ")}\n\n` +
    `Classifiable scenarios:\n${JSON.stringify(classifiable, null, 2)}`;

  const llm = await callSonnetJson({
    system: "You are a precise scanner-predicate writer. Output only valid JSON. Predicate bodies are JavaScript arrow-function source strings.",
    user: userPrompt,
    maxTokens: 16000,
    label: "gen-cell-defs",
  });
  if (!llm.ok) return { ok: false, error: `LLM failed: ${llm.error?.slice(0, 300)}` };

  const j = llm.value || {};
  const predBodies = j.predicates || {};
  const llmPriority = Array.isArray(j.priority) ? j.priority : Object.keys(predBodies);
  const predicates = {};
  const classifiableMap = {};
  for (const [id, val] of Object.entries(predBodies)) {
    if (typeof val === "string") {
      predicates[id] = val;
      classifiableMap[id] = true;  // legacy LLM responses (string-only) assumed classifiable
    } else {
      predicates[id] = val.body || "(c) => false";
      // explicit false ONLY when the LLM declares classifiable=false; default true
      classifiableMap[id] = val.classifiable !== false;
    }
  }

  // PRIORITY only contains classifiable=true ids — classify() should never walk stubs
  const classifiableIds = Object.keys(predicates).filter((id) => classifiableMap[id]);
  const unclassifiableIds = Object.keys(predicates).filter((id) => !classifiableMap[id]);
  const priority = llmPriority.filter((id) => classifiableMap[id]);

  const tsSource = renderCellDefsTs({
    slug, manifestUrl, predicates, priority, classifiableIds, unclassifiableIds,
  });
  const outPath = resolve(ctx.outDir || ctx.repoRoot, "lib", slug, "cell-defs.ts");
  await writeFile(outPath, tsSource, "utf8");

  return {
    ok: true,
    output: {
      cellDefsTsPath: outPath,
      predicateCount: Object.keys(predicates).length,
      classifiableCount: classifiableIds.length,
      unclassifiableCount: unclassifiableIds.length,
      priorityLength: priority.length,
      ids: Object.keys(predicates),
      usage: llm.usage,
    },
  };
}
