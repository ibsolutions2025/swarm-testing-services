/**
 * Step 12 — write-audit-doc + state-machine.ts + index.ts (finalization).
 *
 * 1. Synthesizes clients/<slug>/AUDIT-AND-DESIGN.md from all prior step
 *    outputs via LLM.
 * 2. Emits lib/<slug>/state-machine.ts as a stub `nextRequiredAction`
 *    function that delegates to scenario-driven logic. Phase B v1 leaves
 *    this minimal; real protocols will need iteration.
 * 3. Emits lib/<slug>/index.ts as a barrel re-export of all sibling files.
 */

import { callSonnet } from "../lib/llm.mjs";
import { writeFile, mkdir, readFile } from "node:fs/promises";
import { resolve, dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = dirname(fileURLToPath(import.meta.url));

function renderStateMachineTs({ slug, manifestUrl }) {
  return [
    `// AUTO-GENERATED by framework/onboarding/engine.mjs step 12 (state-machine).`,
    `// Source: ${manifestUrl}`,
    `// Generated: ${new Date().toISOString()}`,
    `// Slug: ${slug}`,
    `//`,
    `// Phase B v1 emits a minimal state machine that returns 'wait' for any`,
    `// non-trivial scenario. Real protocols need a proper nextRequiredAction`,
    `// implementation that knows the lifecycle. For AWP that's hand-written`,
    `// in lib/awp/state-machine.ts; future engine versions can derive it from`,
    `// the contract's state-changing function set.`,
    ``,
    `export type AgentAction =`,
    `  | "claim_validator"`,
    `  | "submit_work"`,
    `  | "approve_submission"`,
    `  | "reject_submission"`,
    `  | "reject_all"`,
    `  | "cancel_job"`,
    `  | "finalize_timed_job"`,
    `  | "submit_review"`,
    `  | "wait"`,
    `  | "none";`,
    ``,
    `export interface DecisionInput {`,
    `  job: { status: number; activeValidator: string; [k: string]: unknown };`,
    `  submissions: Array<{ worker: string; status: number; [k: string]: unknown }>;`,
    `  intendedScenario?: string;`,
    `  configParams: Record<string, unknown>;`,
    `  pendingReviewsOwed?: number;`,
    `  nowSeconds: number;`,
    `}`,
    ``,
    `export interface DecisionResult {`,
    `  action: AgentAction;`,
    `  reason: string;`,
    `  submissionIndex?: number;`,
    `  nextCheckSeconds?: number;`,
    `}`,
    ``,
    `export function nextRequiredAction(input: DecisionInput): DecisionResult {`,
    `  if ((input.pendingReviewsOwed ?? 0) > 0) {`,
    `    return { action: "submit_review", reason: "agent owes reviews; clear them first" };`,
    `  }`,
    `  if (input.job.status === 2) return { action: "none", reason: "job is Completed" };`,
    `  if (input.job.status === 3) return { action: "none", reason: "job is Cancelled" };`,
    `  // Generic fall-through. Engine v1 doesn't infer fine-grained scenario`,
    `  // routing from contract source — that's a Phase B v2 enhancement.`,
    `  return { action: "wait", reason: "no scenario-specific action wired in this engine version", nextCheckSeconds: 60 };`,
    `}`,
    ``,
    `export function isJobStuck(input: DecisionInput): boolean {`,
    `  const r = nextRequiredAction(input);`,
    `  return r.action !== "none" && r.action !== "wait";`,
    `}`,
    ``,
  ].join("\n");
}

