export async function run(ctx) {
  const slug = ctx.steps?.["02-discover-manifest"]?.output?.slug;
  const manifestUrl = ctx.steps?.["02-discover-manifest"]?.output?.manifestUrl;
  if (!slug) return { ok: false, error: "step 12 needs slug" };

  // Build a compact ONBOARDING_REPORT from all prior steps
  const report = {
    slug,
    manifestUrl,
    protocolName: ctx.steps?.["02-discover-manifest"]?.output?.rawName,
    schemaVersion: ctx.steps?.["02-discover-manifest"]?.output?.schemaVersion,
    chain: ctx.steps?.["03-inventory-contracts"]?.output?.chain,
    contracts: ctx.steps?.["03-inventory-contracts"]?.output?.contracts,
    abiFetchSummary: ctx.steps?.["04-fetch-abis"]?.output?.counts,
    abiFailures: (ctx.steps?.["04-fetch-abis"]?.output?.fetchResults || []).filter((r) => !r.ok),
    docs: ctx.steps?.["05-crawl-docs"]?.output,
    mcp: ctx.steps?.["06-audit-mcp"]?.output,
    rules: {
      total: ctx.steps?.["07-generate-rules"]?.output?.totalRules,
      perContract: ctx.steps?.["07-generate-rules"]?.output?.perContract,
    },
    events: {
      count: ctx.steps?.["08-generate-events"]?.output?.uniqueEventCount,
      names: ctx.steps?.["08-generate-events"]?.output?.eventNames,
    },
    matrix: ctx.steps?.["09-derive-matrix"]?.output,
    scenarios: ctx.steps?.["10-derive-scenarios"]?.output,
    cellDefs: ctx.steps?.["11-generate-cell-defs"]?.output,
  };

  const promptPath = resolve(__dirname, "..", "prompts", "write-audit-doc.md");
  const promptTemplate = await readFile(promptPath, "utf8");
  const userPrompt = `${promptTemplate}\n\nONBOARDING_REPORT:\n\n\`\`\`json\n${JSON.stringify(report, null, 2)}\n\`\`\``;

  const llm = await callSonnet({
    system: "You are a precise technical writer. Output Markdown only.",
    user: userPrompt,
    maxTokens: 24000,
    label: "write-audit-doc",
  });
  if (!llm.ok) return { ok: false, error: `LLM failed: ${llm.error?.slice(0, 300)}` };

  // Write outputs (under ctx.outDir — Phase C scratch dir)
  const baseDir = ctx.outDir || ctx.repoRoot;
  const auditDir = resolve(baseDir, "clients", slug);
  await mkdir(auditDir, { recursive: true });
  const auditPath = join(auditDir, "AUDIT-AND-DESIGN.md");
  await writeFile(auditPath, llm.content, "utf8");

  // state-machine.ts + index.ts
  const libDir = resolve(baseDir, "lib", slug);
  await mkdir(libDir, { recursive: true });
  await writeFile(join(libDir, "state-machine.ts"), renderStateMachineTs({ slug, manifestUrl }), "utf8");
  await writeFile(join(libDir, "index.ts"), renderIndexTs({ slug, manifestUrl }), "utf8");

  return {
    ok: true,
    output: {
      auditDocPath: auditPath,
      auditDocLength: llm.content.length,
      stateMachinePath: join(libDir, "state-machine.ts"),
      indexPath: join(libDir, "index.ts"),
      usage: llm.usage,
    },
  };
}
