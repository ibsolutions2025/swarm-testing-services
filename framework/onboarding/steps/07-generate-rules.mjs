/**
 * Step 07 — generate-rules.
 *
 * For each verified contract in the inventory, fetch its Solidity source
 * via chain-explorer getsourcecode + feed to the LLM with extraction
 * prompt. LLM returns structured rule entries; engine concatenates across
 * contracts and emits lib/<slug>/rules.ts.
 *
 * Skips contracts whose source is unverified — records as a finding the
 * engine surfaces in the audit doc (verifying contracts is a docs_product_gap).
 */

import { fetchContractSource } from "../lib/source-fetcher.mjs";
import { callSonnetJson } from "../lib/llm.mjs";
import { writeFile, mkdir, readFile } from "node:fs/promises";
import { resolve, dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = dirname(fileURLToPath(import.meta.url));

function renderRulesTs({ slug, manifestUrl, allRules, errorNamesByContract }) {
  const header = [
    `// AUTO-GENERATED by framework/onboarding/engine.mjs step 07-generate-rules`,
    `// Source: ${manifestUrl}`,
    `// Generated: ${new Date().toISOString()}`,
    `// Slug: ${slug}`,
    `// Coverage: ${allRules.length} rules across ${Object.keys(errorNamesByContract).length} contracts`,
    `//`,
    `// Each rule entry was extracted by an LLM (Kimi K2.6-TEE) from the`,
    `// chain-explorer-verified Solidity source. Manual cross-check expected`,
    `// before treating as authoritative — see clients/<slug>/AUDIT-AND-DESIGN.md`,
    `// section 3 for the auditor's findings backlog.`,
    ``,
    `export type RuleKind = "precondition" | "state" | "gate" | "access" | "constraint";`,
    `export type AuditFailureCategory =`,
    `  | "agent_too_dumb"`,
    `  | "mcp_product_gap"`,
    `  | "docs_product_gap"`,
    `  | "correct_enforcement"`,
    `  | "contract_flaw"`,
    `  | "infra_issue";`,
    ``,
    `export interface Rule {`,
    `  id: string;`,
    `  fn: string;`,
    `  kind: RuleKind;`,
    `  condition: string;`,
    `  errorName: string;`,
    `  failureCategory: AuditFailureCategory;`,
    `  failureSubcategory?: string;`,
    `  notes?: string;`,
    `}`,
    ``,
  ].join("\n");

  const rulesJson = JSON.stringify(allRules, null, 2);
  const body = [
    `export const RULES: Rule[] = ${rulesJson};`,
    ``,
    `export function getRulesForFunction(fnName: string): Rule[] {`,
    `  return RULES.filter((r) => r.fn === fnName);`,
    `}`,
    ``,
    `export const RULE_IDS = RULES.map((r) => r.id);`,
    `export const RULE_COUNT = RULES.length;`,
    ``,
    `// Custom error names by contract (handy for ABI sanity-checks during deploys).`,
    Object.entries(errorNamesByContract)
      .map(([c, names]) => `export const ${c.toUpperCase()}_ERROR_NAMES = ${JSON.stringify(names, null, 2)};`)
      .join("\n\n"),
    ``,
    `const ERROR_NAME_TO_RULES = (() => {`,
    `  const map: Record<string, Rule[]> = {};`,
    `  for (const r of RULES) {`,
    `    if (!map[r.errorName]) map[r.errorName] = [];`,
    `    map[r.errorName].push(r);`,
    `  }`,
    `  return map;`,
    `})();`,
    ``,
    `export function decodeRevertReason(errorNameOrString: string): Rule[] {`,
    `  if (!errorNameOrString) return [];`,
    `  if (ERROR_NAME_TO_RULES[errorNameOrString]) return ERROR_NAME_TO_RULES[errorNameOrString];`,
    `  for (const [name, rules] of Object.entries(ERROR_NAME_TO_RULES)) {`,
    `    if (name.includes(errorNameOrString) || errorNameOrString.includes(name)) return rules;`,
    `  }`,
    `  return [];`,
    `}`,
    ``,
  ].join("\n");

  return header + body;
}

export async function run(ctx) {
  const slug = ctx.steps?.["02-discover-manifest"]?.output?.slug;
  const manifestUrl = ctx.steps?.["02-discover-manifest"]?.output?.manifestUrl;
  const inv = ctx.steps?.["03-inventory-contracts"]?.output;
  if (!slug || !inv) return { ok: false, error: "step 07 needs slug + inventory" };

  const promptPath = resolve(__dirname, "..", "prompts", "generate-rules.md");
  const promptTemplate = await readFile(promptPath, "utf8");

  // The full inventory may be large; limit to contracts we know carry
  // protocol-specific guards. Heuristic: first 4 contracts in the manifest's
  // declared order — the manifest tends to put primary ones first. Engine
  // doesn't know which is "primary" without LLM help, so we go in order
  // and stop after collecting source for the first 4 verified ones to
  // keep prompt + token budget reasonable.
  const target = inv.contracts.slice(0, 8);

  // Pull source-resolver hints from the manifest discovered in step 02.
  const manifest = ctx.steps?.["02-discover-manifest"]?.output?.manifest || {};
  const sourceOpts = {
    manifestRepository: manifest.repository || manifest.repo || null,
    mcpServerRepo: manifest.integration?.mcp_server?.repo || null,
  };

  const allRules = [];
  const errorNamesByContract = {};
  const perContract = [];
  for (const c of target) {
    const src = await fetchContractSource(c.chainId, c.address, { ...sourceOpts, contractName: c.name });
    if (!src.ok) {
      perContract.push({ name: c.name, ok: false, error: src.error });
      continue;
    }
    // Concatenate source files into one payload for the LLM
    const combined = Object.entries(src.files)
      .map(([fn, body]) => `// ===== FILE: ${fn} =====\n${body}`)
      .join("\n\n");
    // Cap at 80k chars to fit in 128k context with prompt + output room
    const sourceForLlm = combined.length > 80000 ? combined.slice(0, 80000) + "\n\n[truncated]" : combined;

    const llm = await callSonnetJson({
      system:
        "You are a precise Solidity auditor. Output only valid JSON of the requested shape. " +
        "Do not invent rules; only emit guards explicitly present in the source.",
      user: `${promptTemplate}\n\nContract name: ${src.contractName || c.name}\nAddress: ${c.address}\nChain: ${c.chainId}\n\n${sourceForLlm}`,
      maxTokens: 32000,
      label: `gen-rules-${c.name}`,
    });
    if (!llm.ok) {
      perContract.push({ name: c.name, ok: false, error: llm.error?.slice(0, 200) });
      continue;
    }
    const rules = Array.isArray(llm.value?.rules) ? llm.value.rules : [];
    perContract.push({ name: c.name, ok: true, ruleCount: rules.length, usage: llm.usage });
    allRules.push(...rules);

    // Collect Solidity custom-error names from the ABI for this contract
    // (engine has them from step 04's emitted contracts.ts)
    const abiName = c.name.replace(/([a-z0-9])([A-Z])/g, "$1_$2").toUpperCase() + "_ABI";
    errorNamesByContract[c.name] = []; // populated by emitter from contracts.ts post-run
  }

  const tsSource = renderRulesTs({ slug, manifestUrl, allRules, errorNamesByContract });
  const outDir = resolve(ctx.outDir || ctx.repoRoot, "lib", slug);
  await mkdir(outDir, { recursive: true });
  const outPath = join(outDir, "rules.ts");
  await writeFile(outPath, tsSource, "utf8");

  // Aggregate per-contract LLM usage so the engine's progress emitter can
  // record cost for this step. Without this, step 07 (the most expensive
  // step at ~$0.27 of a $0.66 run) would log $0 because the emitter reads
  // result.output.usage. See clients/.shared/PHASE-C-DESIGN.md C.3 + the
  // fix-up note from Cowork after Checkpoint 1.
  const usage = perContract.reduce((acc, c) => ({
    input_tokens:               acc.input_tokens               + (c.usage?.input_tokens               || 0),
    output_tokens:              acc.output_tokens              + (c.usage?.output_tokens              || 0),
    cache_creation_input_tokens: acc.cache_creation_input_tokens + (c.usage?.cache_creation_input_tokens || 0),
    cache_read_input_tokens:     acc.cache_read_input_tokens     + (c.usage?.cache_read_input_tokens     || 0),
  }), {
    input_tokens: 0, output_tokens: 0,
    cache_creation_input_tokens: 0, cache_read_input_tokens: 0,
  });

  return {
    ok: true,
    output: {
      rulesTsPath: outPath,
      totalRules: allRules.length,
      perContract,
      usage,
    },
  };
}
