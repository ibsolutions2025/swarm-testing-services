/**
 * Step 04 — fetch-abis.
 *
 * For each contract in the inventory, fetch its ABI from (in order):
 *   1. manifest.abi_endpoints URL (when listed for that contract — fastest,
 *      authoritative for the protocol's intended ABI shape)
 *   2. chain explorer (Etherscan v2 unified, then per-chain Basescan/etc.)
 *
 * Writes lib/<slug>/contracts.ts in a style that matches the hand-written
 * fixture (CONTRACT_ADDRESSES + per-contract <NAME>_ABI exports with
 * `as const` annotations so consumers retain type narrowing).
 *
 * The engine has ZERO knowledge of which contracts are special. Every
 * contract in the manifest's `contracts` map gets an ABI fetch + emit.
 * Fixture-specific helpers (MOCK_USDC_DECIMALS etc.) are emitted as
 * generic helpers ONLY when the engine can derive them from contract
 * call data — for batch 1 we skip helper synthesis entirely; that's a
 * batch 2 enhancement.
 */

import { fetchAbi } from "../lib/abi-fetcher.mjs";
import { writeFile, mkdir } from "node:fs/promises";
import { join, resolve } from "node:path";

function abiNameForExport(contractName) {
  // Convert "JobNFT" → "JOB_NFT_ABI" — same convention the fixture uses.
  // Insert underscore before each capital letter that follows a lowercase
  // letter or digit; uppercase the result; append _ABI.
  const u = String(contractName)
    .replace(/([a-z0-9])([A-Z])/g, "$1_$2")
    .replace(/([A-Z])([A-Z][a-z])/g, "$1_$2")
    .toUpperCase();
  return `${u}_ABI`;
}

/**
 * Render an ABI array as TypeScript source matching the fixture's style.
 * The fixture uses the JSON-style ABI (with `inputs`, `outputs`,
 * `stateMutability`, `name`, `type` etc.) plus `as const` to preserve
 * literal types. We emit the JSON verbatim with `as const` at the end.
 */
function renderAbiAsTs(name, abi) {
  const json = JSON.stringify(abi, null, 2);
  return `export const ${name} = ${json} as const;\n`;
}

function renderContractsTs({ slug, manifestUrl, chain, contracts, abiByName }) {
  const header = [
    `// AUTO-GENERATED by framework/onboarding/engine.mjs`,
    `// Source: ${manifestUrl}`,
    `// Generated: ${new Date().toISOString()}`,
    `// Slug: ${slug}`,
    `//`,
    `// Edits to this file are overwritten on the next engine run. To change`,
    `// the contract inventory or ABIs, update the source manifest at the`,
    `// URL above (or the chain-explorer-verified contract source) and re-run`,
    `// the engine.`,
    ``,
  ].join("\n");

  const addressLines = contracts
    .map((c) => `  ${c.name}: '${c.address}' as \`0x\${string}\`,`)
    .join("\n");

  const addressBlock = [
    `// ─── Contract Addresses (chain: ${chain.name}, chainId: ${chain.id}) ────────`,
    `export const CONTRACT_ADDRESSES = {`,
    addressLines,
    `} as const;`,
    ``,
    `export const NETWORK = {`,
    `  name: '${chain.name}',`,
    `  chainId: ${chain.id},`,
    chain.rpc ? `  rpc: '${chain.rpc}',` : `  rpc: null,`,
    `} as const;`,
    ``,
  ].join("\n");

  const abiBlocks = contracts
    .map((c) => {
      const abi = abiByName[c.name];
      if (!abi) return `// ${c.name}: ABI fetch failed — see runs/<id>/state.json for details`;
      const exportName = abiNameForExport(c.name);
      return [
        `// ─── ${c.name} ABI (${abi.length} entries${c.abiUrl ? `, source: manifest` : `, source: chain explorer`}) ─`,
        renderAbiAsTs(exportName, abi),
      ].join("\n");
    })
    .join("\n");

  return header + addressBlock + abiBlocks;
}

export async function run(ctx) {
  const inv = ctx.steps?.["03-inventory-contracts"]?.output;
  const manifestUrl = ctx.steps?.["02-discover-manifest"]?.output?.manifestUrl;
  const slug = ctx.steps?.["02-discover-manifest"]?.output?.slug;
  if (!inv) return { ok: false, error: "no inventory from step 03" };

  const abiByName = {};
  const fetchResults = [];
  for (const c of inv.contracts) {
    const r = await fetchAbi({
      chainId: c.chainId,
      address: c.address,
      manifestAbiUrl: c.abiUrl || null,
    });
    fetchResults.push({
      name: c.name,
      address: c.address,
      ok: r.ok,
      source: r.source,
      entryCount: r.ok ? r.abi.length : 0,
      error: r.ok ? null : r.error,
    });
    if (r.ok) abiByName[c.name] = r.abi;
  }

  // Render lib/<slug>/contracts.ts (under ctx.outDir — Phase C scratch dir)
  const outDir = resolve(ctx.outDir || ctx.repoRoot, "lib", slug);
  const outPath = join(outDir, "contracts.ts");
  await mkdir(outDir, { recursive: true });

  const tsSource = renderContractsTs({
    slug,
    manifestUrl,
    chain: inv.chain,
    contracts: inv.contracts,
    abiByName,
  });
  await writeFile(outPath, tsSource, "utf8");

  // Summary stats
  const counts = {
    total: inv.contracts.length,
    fetched_ok: fetchResults.filter((r) => r.ok).length,
    fetched_failed: fetchResults.filter((r) => !r.ok).length,
    by_source: fetchResults.reduce((acc, r) => {
      if (!r.ok) return acc;
      acc[r.source] = (acc[r.source] || 0) + 1;
      return acc;
    }, {}),
  };

  return {
    ok: true,
    output: {
      contractsTsPath: outPath,
      slug,
      counts,
      fetchResults,
      abiByName_keys: Object.keys(abiByName),
    },
  };
}
