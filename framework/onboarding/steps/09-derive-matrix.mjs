/**
 * Step 09 — derive-matrix.
 *
 * Find the protocol's primary "create" function in its main contract,
 * feed its ABI + nearby contract source to the LLM, and let the LLM
 * derive the configuration axes. Emit lib/<slug>/matrix.ts with axes,
 * config-key format, and a generator for valid combinations.
 *
 * Heuristic for "primary contract": the manifest's first contract entry,
 * or the one whose ABI has the most functions (likely the marketplace
 * core). We don't hardcode names — the LLM can read the ABI and decide
 * which function is the "create" one.
 */

import { callSonnetJson } from "../lib/llm.mjs";
import { fetchContractSource } from "../lib/source-fetcher.mjs";
import { writeFile, mkdir, readFile } from "node:fs/promises";
import { resolve, dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = dirname(fileURLToPath(import.meta.url));

function pickPrimaryContract(inv, abiBundle) {
  // Score by function count; pick the highest. If tied, pick the first.
  let best = null;
  let bestScore = -1;
  for (const c of inv.contracts) {
    const abiName = c.name.replace(/([a-z0-9])([A-Z])/g, "$1_$2").toUpperCase() + "_ABI";
    const abi = abiBundle[abiName];
    if (!Array.isArray(abi)) continue;
    const score = abi.filter((e) => e.type === "function").length;
    if (score > bestScore) { bestScore = score; best = c; }
  }
  return best;
}

function pickCreateFunction(abi) {
  // Find functions whose name starts with "create" and have many args
  // (a configurable create function is the matrix-defining one).
  if (!Array.isArray(abi)) return null;
  const creates = abi.filter(
    (e) => e.type === "function" && /^create/i.test(e.name || "")
  );
  if (!creates.length) return null;
  creates.sort((a, b) => (b.inputs || []).length - (a.inputs || []).length);
  return creates[0];
}

function renderMatrixTs({ slug, manifestUrl, axes, constraints, configKeyFormat }) {
  const header = [
    `// AUTO-GENERATED by framework/onboarding/engine.mjs step 09-derive-matrix`,
    `// Source: ${manifestUrl}`,
    `// Generated: ${new Date().toISOString()}`,
    `// Slug: ${slug}`,
    `//`,
    `// Axes derived by LLM from the primary create-function ABI + source.`,
    `// Manual review expected before treating as authoritative.`,
    ``,
  ].join("\n");

  return header + [
    `export const AXES = ${JSON.stringify(axes, null, 2)} as const;`,
    ``,
    `export const AXIS_CONSTRAINTS = ${JSON.stringify(constraints, null, 2)};`,
    ``,
    `export const CONFIG_KEY_FORMAT = ${JSON.stringify(configKeyFormat)};`,
    ``,
    `// Generated configs — Cartesian product respecting axis constraints.`,
    `// Engine v1: emits the product without enforcing constraints; fixture-`,
    `// authoritative ALL_CONFIGS list lives in the diff harness expected output.`,
    `function generateConfigs(): string[] {`,
    `  const out: string[] = [];`,
    `  function walk(axisIdx: number, parts: string[]) {`,
    `    if (axisIdx >= AXES.length) {`,
    `      out.push(parts.join("-"));`,
    `      return;`,
    `    }`,
    `    const axis = AXES[axisIdx];`,
    `    for (const v of axis.values) walk(axisIdx + 1, [...parts, v]);`,
    `  }`,
    `  walk(0, []);`,
    `  return out;`,
    `}`,
    ``,
    `export const ALL_CONFIGS_RAW: string[] = generateConfigs();`,
    `// Filter out combinations that violate documented constraints.`,
    `// Constraints are advisory at this engine version; downstream consumers`,
    `// should call isConfigValid() before using a key.`,
    `export function isConfigValid(key: string): boolean {`,
    `  // Simple constraint matcher — extend as constraints get more complex.`,
    `  for (const c of AXIS_CONSTRAINTS) {`,
    `    const rule = c.rule || "";`,
    `    // Constraints in the form "X=A ⇒ Y=B" → if key matches X=A, must have Y=B`,
    `    const m = /(\\S+)\\s*=\\s*(\\S+)\\s*⇒\\s*(\\S+)\\s*=\\s*(\\S+)/.exec(rule);`,
    `    if (!m) continue;`,
    `    const parts = key.split("-");`,
    `    const axisIdx = AXES.findIndex((a) => a.name === m[1] || a.values.includes(m[1]));`,
    `    if (axisIdx < 0) continue;`,
    `    if (parts[axisIdx] === m[2]) {`,
    `      const reqAxisIdx = AXES.findIndex((a) => a.name === m[3] || a.values.includes(m[3]));`,
    `      if (reqAxisIdx < 0) continue;`,
    `      if (parts[reqAxisIdx] !== m[4]) return false;`,
    `    }`,
    `  }`,
    `  return true;`,
    `}`,
    ``,
    `export const ALL_CONFIGS = ALL_CONFIGS_RAW.filter(isConfigValid);`,
    `export const CONFIG_COUNT = ALL_CONFIGS.length;`,
    ``,
  ].join("\n");
}

export async function run(ctx) {
  const slug = ctx.steps?.["02-discover-manifest"]?.output?.slug;
  const manifestUrl = ctx.steps?.["02-discover-manifest"]?.output?.manifestUrl;
  const inv = ctx.steps?.["03-inventory-contracts"]?.output;
  if (!slug || !inv) return { ok: false, error: "step 09 needs slug + inventory" };

  // Re-extract abi bundle from the emitted contracts.ts (under ctx.outDir)
  const baseDir = ctx.outDir || ctx.repoRoot;
  const contractsTsPath = resolve(baseDir, "lib", slug, "contracts.ts");
  const src = await readFile(contractsTsPath, "utf8");
  const re = /export const (\w+_ABI)\s*=\s*\[([\s\S]*?)\] as const;/g;
  const abiBundle = {};
  let m;
  while ((m = re.exec(src)) !== null) {
    try {
      abiBundle[m[1]] = JSON.parse(`[${m[2]}]`);
    } catch (e) {
      // skip bad parse
    }
  }

  const primary = pickPrimaryContract(inv, abiBundle);
  if (!primary) return { ok: false, error: "could not pick primary contract" };
  const primaryAbiName = primary.name.replace(/([a-z0-9])([A-Z])/g, "$1_$2").toUpperCase() + "_ABI";
  const primaryAbi = abiBundle[primaryAbiName];
  const createFn = pickCreateFunction(primaryAbi);
  if (!createFn) return { ok: false, error: `no create-style function in ${primary.name}` };

  // Pull source for primary contract to give LLM context
  const manifest = ctx.steps?.["02-discover-manifest"]?.output?.manifest || {};
  const sourceRes = await fetchContractSource(primary.chainId, primary.address, {
    contractName: primary.name,
    manifestRepository: manifest.repository || manifest.repo || null,
    mcpServerRepo: manifest.integration?.mcp_server?.repo || null,
  });
  const srcExcerpt = sourceRes.ok
    ? Object.entries(sourceRes.files).map(([f, b]) => `// ===== ${f} =====\n${b}`).join("\n\n").slice(0, 60000)
    : "(source unavailable; LLM will derive from ABI alone)";

  const promptPath = resolve(__dirname, "..", "prompts", "derive-matrix.md");
  const promptTemplate = await readFile(promptPath, "utf8");
  const userPrompt =
    `${promptTemplate}\n\nPrimary contract: ${primary.name} (${primary.address})\n` +
    `Create function: ${createFn.name}(${(createFn.inputs || []).map((i) => `${i.type} ${i.name || ""}`).join(", ")})\n\n` +
    `Full ABI of create function:\n${JSON.stringify(createFn, null, 2)}\n\n` +
    `Contract source (excerpt):\n${srcExcerpt}`;

  const llm = await callSonnetJson({
    system: "You are a precise protocol auditor. Output only valid JSON of the requested shape.",
    user: userPrompt,
    maxTokens: 16000,
    label: "derive-matrix",
  });
  if (!llm.ok) return { ok: false, error: `LLM failed: ${llm.error?.slice(0, 300)}` };

  const j = llm.value || {};
  const axes = Array.isArray(j.axes) ? j.axes : [];
  const constraints = Array.isArray(j.constraints) ? j.constraints : [];
  const configKeyFormat = j.config_key_format || axes.map((a) => `{${a.name}}`).join("-");

  const tsSource = renderMatrixTs({ slug, manifestUrl, axes, constraints, configKeyFormat });
  const outPath = resolve(baseDir, "lib", slug, "matrix.ts");
  await writeFile(outPath, tsSource, "utf8");

  return {
    ok: true,
    output: {
      matrixTsPath: outPath,
      primaryContract: primary.name,
      createFunction: createFn.name,
      axesCount: axes.length,
      axisNames: axes.map((a) => a.name),
      constraintsCount: constraints.length,
      estimatedTotalConfigs: j.estimated_total_configs || null,
      configKeyFormat,
      usage: llm.usage,
    },
  };
}
