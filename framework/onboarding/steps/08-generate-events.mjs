/**
 * Step 08 — generate-events.
 *
 * Mechanical: walk every ABI fetched in step 04, collect every entry
 * where type=="event", and emit lib/<slug>/events.ts with:
 *   - EVENT_SIGNATURES: canonical Solidity signatures ("Name(type1,type2)")
 *   - EVENT_SIGS:      keccak256 topic0 hashes per event name
 *   - SIG_TO_NAME:     reverse map
 *   - decodeEvent(log) — minimal indexed-arg extractor
 *   - groupEvents(logs) — sort + group helper
 *
 * No LLM. The fixture's events.ts (lib/awp/events.ts) is the spec target;
 * key invariant is byte-identical topic0 hashes vs the fixture's pinned
 * 12 hashes (computed from canonical Solidity signatures via keccak256).
 *
 * The fixture's pinned signatures come from contracts/JobNFTv15.sol +
 * ReviewGateV4.sol. Engine emits canonical signatures derived directly
 * from each event's ABI inputs (just like solc would).
 */

import { writeFile, mkdir } from "node:fs/promises";
import { join, resolve } from "node:path";
import { keccak256, toBytes } from "viem";

/**
 * Build the canonical Solidity signature for an event ABI entry.
 *   { name: 'JobCreated', inputs: [{type: 'uint256'}, {type: 'address'}] }
 *     → "JobCreated(uint256,address)"
 *
 * For tuple/struct args, recursively descend into components.
 */
function canonicalEventSig(entry) {
  const inner = (entry.inputs || []).map((i) => abiTypeToCanonical(i)).join(",");
  return `${entry.name}(${inner})`;
}

function abiTypeToCanonical(input) {
  // Solidity supports nested tuples — handle them recursively.
  if (input.type === "tuple") {
    const inner = (input.components || []).map((c) => abiTypeToCanonical(c)).join(",");
    return `(${inner})`;
  }
  if (input.type === "tuple[]") {
    const inner = (input.components || []).map((c) => abiTypeToCanonical(c)).join(",");
    return `(${inner})[]`;
  }
  // Fixed-size tuple arrays e.g. tuple[3]
  const m = /^tuple\[(\d+)\]$/.exec(input.type);
  if (m) {
    const inner = (input.components || []).map((c) => abiTypeToCanonical(c)).join(",");
    return `(${inner})[${m[1]}]`;
  }
  return input.type;
}

function eventNameToTopic0(sig) {
  return keccak256(toBytes(sig));
}

function uniqByName(events) {
  // ABIs may declare the same event in multiple contracts (e.g. ERC20 Transfer).
  // Keep the first occurrence; downstream consumers don't need duplicates.
  const seen = new Set();
  const out = [];
  for (const e of events) {
    if (seen.has(e.name)) continue;
    seen.add(e.name);
    out.push(e);
  }
  return out;
}

/**
 * Render events.ts in a style close to lib/awp/events.ts.
 */
function renderEventsTs({ slug, manifestUrl, contracts, allEvents, sigToName, eventSigs, eventSignatures }) {
  const eventNames = allEvents.map((e) => e.name);
  const typeLines = eventNames.map((n) => `  | "${n}"`).join("\n");

  const sigsLines = Object.entries(eventSignatures)
    .map(([name, sig]) => `  ${name}: "${sig}",`)
    .join("\n");

  const hashesLines = Object.entries(eventSigs)
    .map(([name, hash]) => `  ${name}: "${hash}",`)
    .join("\n");

  return [
    `// AUTO-GENERATED by framework/onboarding/engine.mjs step 08-generate-events`,
    `// Source: ${manifestUrl}`,
    `// Generated: ${new Date().toISOString()}`,
    `// Slug: ${slug}`,
    `// Coverage: ${allEvents.length} unique events across ${contracts.length} contracts`,
    `//`,
    `// topic0 hashes are keccak256 of the canonical Solidity event signature`,
    `// (e.g. keccak256(toBytes("JobCreated(uint256,address,uint256)"))).`,
    ``,
    `import { keccak256, toBytes } from "viem";`,
    ``,
    `export type EventName =`,
    typeLines,
    `  ;`,
    ``,
    `export const EVENT_SIGNATURES: Record<EventName, string> = {`,
    sigsLines,
    `};`,
    ``,
    `export const EVENT_SIGS: Record<EventName, \`0x\${string}\`> = {`,
    hashesLines,
    `};`,
    ``,
    `export const SIG_TO_NAME: Record<string, EventName> = (() => {`,
    `  const map: Record<string, EventName> = {};`,
    `  for (const [name, sig] of Object.entries(EVENT_SIGS) as [EventName, \`0x\${string}\`][]) {`,
    `    map[sig.toLowerCase()] = name;`,
    `  }`,
    `  return map;`,
    `})();`,
    ``,
    `export function computeTopic0(eventSignature: string): \`0x\${string}\` {`,
    `  return keccak256(toBytes(eventSignature)) as \`0x\${string}\`;`,
    `}`,
    ``,
    `export interface RawLog {`,
    `  address: \`0x\${string}\`;`,
    `  topics: \`0x\${string}\`[];`,
    `  data: \`0x\${string}\`;`,
    `  blockNumber: \`0x\${string}\` | bigint | number;`,
    `  transactionHash: \`0x\${string}\`;`,
    `  logIndex?: \`0x\${string}\` | number;`,
    `}`,
    ``,
    `export interface DecodedEvent {`,
    `  name: EventName;`,
    `  topic0: \`0x\${string}\`;`,
    `  blockNumber: number;`,
    `  txHash: \`0x\${string}\`;`,
    `  rawTopics: \`0x\${string}\`[];`,
    `  rawData: \`0x\${string}\`;`,
    `  jobId?: bigint;`,
    `  worker?: \`0x\${string}\`;`,
    `  validator?: \`0x\${string}\`;`,
    `  poster?: \`0x\${string}\`;`,
    `}`,
    ``,
    `function topicToBigInt(topic: \`0x\${string}\` | undefined): bigint | undefined {`,
    `  return topic ? BigInt(topic) : undefined;`,
    `}`,
    ``,
    `function topicToAddress(topic: \`0x\${string}\` | undefined): \`0x\${string}\` | undefined {`,
    `  if (!topic) return undefined;`,
    `  const hex = topic.toLowerCase().replace(/^0x/, "");`,
    `  if (hex.length !== 64) return undefined;`,
    `  return ("0x" + hex.slice(24)) as \`0x\${string}\`;`,
    `}`,
    ``,
    `export function decodeEvent(log: RawLog): DecodedEvent | null {`,
    `  const topic0 = log.topics?.[0]?.toLowerCase();`,
    `  if (!topic0) return null;`,
    `  const name = SIG_TO_NAME[topic0];`,
    `  if (!name) return null;`,
    `  const blockNumber =`,
    `    typeof log.blockNumber === "bigint" ? Number(log.blockNumber)`,
    `    : typeof log.blockNumber === "number" ? log.blockNumber`,
    `    : Number(BigInt(log.blockNumber));`,
    `  // First indexed topic (after topic0) is conventionally the primary id.`,
    `  return {`,
    `    name,`,
    `    topic0: topic0 as \`0x\${string}\`,`,
    `    blockNumber,`,
    `    txHash: log.transactionHash,`,
    `    rawTopics: log.topics,`,
    `    rawData: log.data,`,
    `    jobId: topicToBigInt(log.topics[1]),`,
    `    worker: topicToAddress(log.topics[2]),`,
    `    validator: topicToAddress(log.topics[2]),`,
    `    poster: topicToAddress(log.topics[2]),`,
    `  };`,
    `}`,
    ``,
    `export function groupEvents(logs: RawLog[]): Record<string, DecodedEvent[]> {`,
    `  const out: Record<string, DecodedEvent[]> = {};`,
    `  for (const log of logs) {`,
    `    const dec = decodeEvent(log);`,
    `    if (!dec) continue;`,
    `    if (!out[dec.name]) out[dec.name] = [];`,
    `    out[dec.name].push(dec);`,
    `  }`,
    `  for (const arr of Object.values(out)) {`,
    `    arr.sort((a, b) => a.blockNumber - b.blockNumber);`,
    `  }`,
    `  return out;`,
    `}`,
    ``,
    `export const ALL_EVENT_NAMES: EventName[] = Object.keys(EVENT_SIGS) as EventName[];`,
    ``,
  ].join("\n");
}

export async function run(ctx) {
  const slug = ctx.steps?.["02-discover-manifest"]?.output?.slug;
  const manifestUrl = ctx.steps?.["02-discover-manifest"]?.output?.manifestUrl;
  const inv = ctx.steps?.["03-inventory-contracts"]?.output;
  const fetchResults = ctx.steps?.["04-fetch-abis"]?.output?.fetchResults || [];
  if (!slug || !inv) return { ok: false, error: "step 08 needs slug + inventory + ABIs" };

  // We need the full ABI bodies. Step 04 stored them only to disk (lib/<slug>/contracts.ts
  // under ctx.outDir). Re-fetch them from there.
  const baseDir = ctx.outDir || ctx.repoRoot;
  const contractsModulePath = resolve(baseDir, "lib", slug, "contracts.ts");

  // Load + parse the contracts.ts module manually since Node ESM can't
  // import .ts directly. We read the file and extract each `<NAME>_ABI = [...]`
  // block via the same balanced-bracket scan validate-against-fixture uses.
  const { readFile } = await import("node:fs/promises");
  const src = await readFile(contractsModulePath, "utf8");

  function extractAbis(s) {
    const out = {};
    const re = /export const (\w+_ABI)\s*=\s*\[/g;
    let m;
    while ((m = re.exec(s)) !== null) {
      const name = m[1];
      const start = m.index + m[0].length - 1;
      let depth = 0, i = start, inStr = false, strCh = null, lc = false, bc = false;
      for (; i < s.length; i++) {
        const c = s[i], c2 = s[i + 1] || "";
        if (lc) { if (c === "\n") lc = false; continue; }
        if (bc) { if (c === "*" && c2 === "/") { bc = false; i++; } continue; }
        if (inStr) { if (c === "\\") { i++; continue; } if (c === strCh) { inStr = false; strCh = null; } continue; }
        if (c === "/" && c2 === "/") { lc = true; i++; continue; }
        if (c === "/" && c2 === "*") { bc = true; i++; continue; }
        if (c === '"' || c === "'") { inStr = true; strCh = c; continue; }
        if (c === "[") depth++;
        else if (c === "]") { depth--; if (depth === 0) { i++; break; } }
      }
      let body = s.slice(start, i).replace(/\] as const;/g, "];");
      try {
        out[name] = JSON.parse(body);
      } catch (e) {
        try {
          // eslint-disable-next-line no-new-func
          out[name] = Function(`"use strict"; return (${body});`)();
        } catch (e2) {
          out[name] = { error: e2.message };
        }
      }
    }
    return out;
  }

  const abiBundle = extractAbis(src);

  // Collect every event entry, dedupe by name (Transfer/Approval appear in
  // every ERC20 — keep first sighting).
  const allEvents = [];
  for (const [exportName, abi] of Object.entries(abiBundle)) {
    if (!Array.isArray(abi)) continue;
    for (const entry of abi) {
      if (entry && entry.type === "event" && typeof entry.name === "string") {
        allEvents.push({ name: entry.name, sourceContract: exportName, inputs: entry.inputs || [] });
      }
    }
  }
  const unique = uniqByName(allEvents);

  const eventSignatures = {};
  const eventSigs = {};
  const sigToName = {};
  for (const e of unique) {
    const sig = canonicalEventSig(e);
    const hash = eventNameToTopic0(sig);
    eventSignatures[e.name] = sig;
    eventSigs[e.name] = hash;
    sigToName[hash.toLowerCase()] = e.name;
  }

  const tsSource = renderEventsTs({
    slug, manifestUrl,
    contracts: inv.contracts,
    allEvents: unique,
    sigToName,
    eventSigs,
    eventSignatures,
  });

  const outDir = resolve(baseDir, "lib", slug);
  await mkdir(outDir, { recursive: true });
  const outPath = join(outDir, "events.ts");
  await writeFile(outPath, tsSource, "utf8");

  return {
    ok: true,
    output: {
      eventsTsPath: outPath,
      uniqueEventCount: unique.length,
      duplicates: allEvents.length - unique.length,
      eventNames: unique.map((e) => e.name),
      sampleSignature: unique[0] ? `${unique[0].name} → ${eventSignatures[unique[0].name]}` : null,
    },
  };
}
